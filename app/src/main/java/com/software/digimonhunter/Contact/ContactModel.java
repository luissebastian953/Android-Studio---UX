package com.software.digimonhunter.Contact;

public class ContactModel {

    private int ImageContactDrawer;
    private String ContactDescription;
    private String ContactCall;

    public int getImageContactDrawer() {
        return ImageContactDrawer;
    }

    public void setImageContactDrawer(int imageContactDrawer) {
        ImageContactDrawer = imageContactDrawer;
    }

    public String getContactDescription() {
        return ContactDescription;
    }

    public void setContactDescription(String contactDescription) {
        ContactDescription = contactDescription;
    }

    public String getContactCall() {
        return ContactCall;
    }

    public void setContactCall(String contactCall) {
        ContactCall = contactCall;
    }

}
